package main.java.DesignPatterns.Behavioural.Observer;

public class ScoreBoard {

    int runs;
    int wickets;

    public int getRuns() {
        return runs;
    }

    public void setRuns(int runs) {
        this.runs = runs;
    }

    public int getWickets() {
        return wickets;
    }

    public void setWickets(int wickets) {
        this.wickets = wickets;
    }
}
